/* Simple delay. */
void sleep(int val) {
	int i;
	for(i = val; i > 0; i--);
}

uint8_t spi_send_recv(uint8_t data) {
	/* Wait until SPI transmit buffer is empty. */
	while(!(SPI2STAT & 0x08));

	/* put data in the data buffer. */
	SPI2BUF = data;

	/* Wait until Receive buffer is full. */
	while(!(SPI2STAT & 1));

	/* return the data in the data buffer. */
	return SPI2BUF;
}

/* Initializes the display with predefined commands. */
void display_init(void) {
  	DISPLAY_CHANGE_TO_COMMAND_MODE;
	sleep(10);
	DISPLAY_ACTIVATE_VDD;
	sleep(1000000);
	
	spi_send_recv(0xAE);
	DISPLAY_ACTIVATE_RESET;
	sleep(10);
	DISPLAY_DO_NOT_RESET;
	sleep(10);
	
	spi_send_recv(0x8D);
	spi_send_recv(0x14);
	
	spi_send_recv(0xD9);
	spi_send_recv(0xF1);
	
	DISPLAY_ACTIVATE_VBAT;
	sleep(10000000);
	
	spi_send_recv(0xA1);
	spi_send_recv(0xC8);
	
	spi_send_recv(0xDA);
	spi_send_recv(0x20);
	
	spi_send_recv(0xAF);
}

void display_string(int line, char *s) {
	/* Check if line is valid segment on display and the string s is not NULL. */
    if (line < 0 || line >= 4) return;
    if (!s) return;

	int i;
	/* Put the string in the row according to line variable. */
    for (i = 0; i < 16; i++) {
        if (*s){
            text_buffer[line][i] = *s;
            s++;
        } else {
            text_buffer[line][i] = ' ';
        }
    }
}

void display_update() {
	int i, j, k, c;
    for (i = 0; i < 4; i++) {
		DISPLAY_CHANGE_TO_COMMAND_MODE;

		/* Set page address. */
        spi_send_recv(0x22);  // set page command
        spi_send_recv(i);	  // page number

		/* Start at the left column. */
        spi_send_recv(0x0);  // low nybble of column
        spi_send_recv(0x10); // high nybble of column

		DISPLAY_CHANGE_TO_DATA_MODE;

        for (j = 0; j < 16; j++)  {
            c = text_buffer[i][j];
            if (c & 0x80) continue;

			/* Sends a row in the fonts array to the display. 
			It will be an 8x8 bitmap that creates each character. */
            for (k = 0; k < 8; k++) {
                spi_send_recv(font[c * 8 + k]);
            }
        }
    }
}

void display_initial() {
    int i, j;
    char buffer[256];
	period_unit = 0;

	/* Loop over the entries in the menu_items array. */
    for (i = 0; i < sizeof(menu_items) / sizeof(menu_items[0]); i++) {
        if (i == menu_selection) {
			/* Only for the specified menu_selection will have the '>' symbol
			and not on the rest of them. */
            buffer[0] = '>';
            buffer[1] = ' ';
			
			/* Iterate over each character in the current string. */
            for (j = 0; menu_items[i][j] != '\0'; j++) {
				/* Add the character to the buffer. */
                buffer[j+2] = menu_items[i][j];
            }
            buffer[j+2] = '\0';
            display_string(i, buffer);
        } else {
            display_string(i, menu_items[i]);
        }
    }
    display_update();
    sleep(1000000);
}

/* Displays min, max, avg information on the display*/
void display_info() {
	period_calc(period_time, &avg, &min, &max);
    float_to_string(avg);
	period_unit_char();
    strcat(string_buffer, "Average");
    display_string(0, string_buffer);
    float_to_string(max);
	period_unit_char();
    strcat(string_buffer, "Max");
    display_string(1, string_buffer);
    float_to_string(min);
	period_unit_char();
    strcat(string_buffer, "Min");
    display_string(2, string_buffer);
    display_update();
}

/* Displays the temperature on the display. */
void display_temperature(float temp, const char *unit) {
    float_to_string(temp);
	
	/* Concatinate the unit that was passed to the function. */
    strcat(string_buffer, " ");
    strcat(string_buffer, unit);
	
    display_string(0, "Temperature:");
    display_string(1, string_buffer);
    display_string(2, "");
    display_string(3, "");
    display_update();
    sleep(1000000);
}